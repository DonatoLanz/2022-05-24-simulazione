package it.polito.tdp.itunes.model;

public class Coppia {
  private int t1;
  private int t2;
  private double peso;

  public Coppia(int t1, int t2, double peso) {
	super();
	this.t1 = t1;
	this.t2 = t2;
	this.peso = peso;
}

public int getT1() {
	return t1;
}

public void setT1(int t1) {
	this.t1 = t1;
}

public int getT2() {
	return t2;
}

public void setT2(int t2) {
	this.t2 = t2;
}

public double getPeso() {
	return peso;
}

public void setPeso(double peso) {
	this.peso = peso;
}
  
  
  
  
}
